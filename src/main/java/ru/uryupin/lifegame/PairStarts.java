package ru.uryupin.lifegame;

class PairStarts {

    private final int start;
    private final int offset;

    PairStarts(int start, int offset) {
        this.start = start;
        this.offset = offset;
    }

    int getStart() {

        return start;
    }

    int getOffset() {

        return offset;
    }
}
